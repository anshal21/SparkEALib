    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       double m= Double.parseDouble(jTextField1.getText());
       double c= Double.parseDouble(jTextField2.getText());
      // double s= Double.parseDouble(jTextField3.getText());
      int w= Integer.parseInt(jTextField3.getText());
       double e= Double.parseDouble(jTextField6.getText());
       int i= virtualMachines.size();
       VirtualMachine vm = new VirtualMachine(i,m,c,0,e,w);
       virtualMachines.add(vm);
       if(e!=0){
           failingVirtualMachines.add(vm);
       }
       mod.addRow(new Object[]{i,m,c,e,w});
       jTable1.setModel(mod);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        requests.clear();
        int open = jFileChooser1.showOpenDialog(null);
        if (open == jFileChooser1.APPROVE_OPTION){
            BufferedReader br = null;
            try {
                File selectedFile = jFileChooser1.getSelectedFile();
                br = new BufferedReader(new FileReader(selectedFile));
                String st;
                while ((st = br.readLine()) != null){
                    int s = Integer.parseInt(st);
                    int id = requests.size();
                    requests.add(new Request(id, s));
                }
            } catch (FileNotFoundException ex) {
                System.out.println("File not found.");
            } catch (IOException ex) {
                System.out.println("File not found.");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

