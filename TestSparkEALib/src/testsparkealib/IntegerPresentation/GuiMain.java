       AdvanceDistributor ad = new AdvanceDistributor("Distributor","spark://bluescorpion:7077");
        IntegerChromosome bestChromosome = (IntegerChromosome)ad.distribute(work, pf, params);
        bestChromosome.print();
        System.out.println(20000-bestChromosome.getFitnessValue());
       
