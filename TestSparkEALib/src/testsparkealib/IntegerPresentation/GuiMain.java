        int p=Integer.parseInt(jTextField4.getText());
        int i = Integer.parseInt(jTextField5.getText());
        int f = jComboBox1.getSelectedIndex();
        int pf = Integer.parseInt(Parallel.getText());
        Acessories ac = new Acessories();
        ac.input(virtualMachines, requests);
        Genetic gen = new Genetic(f);
        Worker work = new Worker(gen, ac, p, requests.size(), 0, virtualMachines.size()-1);
        //SimpleDistributor sd = new SimpleDistributor("Distributor", "spark://bluescorpion:7077");
